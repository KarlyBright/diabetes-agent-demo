def get_guidelines_for_complications(complications: list[str] | None) -> str:
    if not complications:
        return ""
    
    lines = []
    for comp in complications:
        if comp in DIABETES_DIET_GUIDELINES["特殊情况"]:
            lines.append(f"【{comp}】")
            for item in DIABETES_DIET_GUIDELINES["特殊情况"][comp]:
                lines.append(f"  - {item}")
    
    return "\n".join(lines)


def get_food_recommendations(food_type: str) -> str:
    if food_type == "推荐":
        return "、".join(DIABETES_DIET_GUIDELINES["食物选择"]["推荐"][:8])
    elif food_type == "限制":
        return "、".join(DIABETES_DIET_GUIDELINES["食物选择"]["限制"][:6])
    elif food_type == "避免":
        return "、".join(DIABETES_DIET_GUIDELINES["食物选择"]["避免"][:6])
    return ""
